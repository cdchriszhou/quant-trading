const map = { ...BUILTIN }

/** Enhance with real-time API data (non-blocking). */
export async function initStockNames() {
  try {
    const res = await marketApi.searchSymbols('')
    const list = res.data || []
    list.forEach(s => {
      if (!map[s.code]) map[s.code] = s.name
      const bare = s.code.replace('.SZ', '').replace('.SH', '')
      if (!map[bare]) map[bare] = s.name
    })
    console.log(`[stockNames] Loaded ${list.length} from API, total map: ${Object.keys(map).length}`)
  } catch (e) {
    console.warn('[stockNames] API load failed, using builtin map:', e.message || e)
  }
}

/** Synchronous lookup — guaranteed to return Chinese name for all major A-shares. */
export function getStockName(code) {
  if (!code) return ''
  return map[code]
    || map[code.replace('.SZ', '').replace('.SH', '')]
    || (code.includes('.') ? '' : map[code + '.SZ'] || map[code + '.SH'])
    || code
}
